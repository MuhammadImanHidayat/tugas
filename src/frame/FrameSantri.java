    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    try {
            int induk = Integer.parseInt(jTextField4.getText()); 
            String nama = jTextField1.getText();                 
            String alamat = jTextField2.getText();               
            String kamar = jTextField3.getText();                

            drivers.Main.daftarSantri.add(new activities.Santri(induk, nama, alamat, kamar));

            javax.swing.JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan!");

            tampilkanKeTabel(); 
            bersihkanForm();
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "No Induk harus berupa angka!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed
private void tampilkanKeTabel() {
        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel();
        model.addColumn("No Induk");
        model.addColumn("Nama Santri");
        model.addColumn("Alamat");
        model.addColumn("Kamar");
        
        for (activities.Santri s : drivers.Main.daftarSantri) {
            Object[] barisData = { s.getInduk(), s.getNama(), s.getAlamat(), s.getKamar() };
            model.addRow(barisData);
        }
        jTable3.setModel(model); 
    }
private void bersihkanForm() {
        jTextField4.setText(""); 
        jTextField1.setText(""); 
        jTextField2.setText(""); 
        jTextField3.setText(""); 
        barisTerpilih = -1;
    }
