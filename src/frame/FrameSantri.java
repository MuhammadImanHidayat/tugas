    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    // TODO add your handling code here:
    if (barisTerpilih == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih baris di tabel jTable3 dulu!");
            return;
        }
        int konfirmasi = javax.swing.JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus data santri ini?", "Konfirmasi", javax.swing.JOptionPane.YES_NO_OPTION);
        if (konfirmasi == javax.swing.JOptionPane.YES_OPTION) {
            drivers.Main.daftarSantri.remove(barisTerpilih);
            tampilkanKeTabel(); 
            bersihkanForm();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jTable3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable3MouseClicked
barisTerpilih = jTable3.getSelectedRow();
        if (barisTerpilih != -1) {
            jTextField4.setText(jTable3.getValueAt(barisTerpilih, 0).toString()); 
            jTextField1.setText(jTable3.getValueAt(barisTerpilih, 1).toString()); 
            jTextField2.setText(jTable3.getValueAt(barisTerpilih, 2).toString()); 
            jTextField3.setText(jTable3.getValueAt(barisTerpilih, 3).toString()); 
        }
    }//GEN-LAST:event_jTable3MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
if (barisTerpilih == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih baris di tabel jTable3 dulu!");
            return;
        }
        try {
            int induk = Integer.parseInt(jTextField4.getText());
            String nama = jTextField1.getText();
            String alamat = jTextField2.getText();
            String kamar = jTextField3.getText();

            activities.Santri santri = drivers.Main.daftarSantri.get(barisTerpilih);
            santri.setInduk(induk);
            santri.setNama(nama);
            santri.setAlamat(alamat);
            santri.setKamar(kamar);

            javax.swing.JOptionPane.showMessageDialog(this, "Data Berhasil Diubah!");
            tampilkanKeTabel(); 
            bersihkanForm();
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "No Induk harus berupa angka!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

